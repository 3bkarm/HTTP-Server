package request

import (
	"io"
	"testing"

	"github.com/stretchr/testify/assert"
	"github.com/stretchr/testify/require"
)

type chunkReader struct {
	data            string
	numBytesPerRead int
	pos             int
}

// Read reads up to len(p) or numBytesPerRead bytes from the string per call
// its useful for simulating reading a variable number of bytes per chunk from a network connection
func (cr *chunkReader) Read(p []byte) (n int, err error) {
	if cr.pos >= len(cr.data) {
		return 0, io.EOF
	}

	endIndex := min(cr.pos+cr.numBytesPerRead, len(cr.data))
	n = copy(p, cr.data[cr.pos:endIndex])
	cr.pos += n

	return n, nil
}

func TestRequestLineParse(t *testing.T) {
	// Test: Good GET Request line
	reader := &chunkReader{
		data:            "GET / HTTP/1.1\r\nHost: localhost:42069\r\nUser-Agent: curl/7.81.0\r\nAccept: */*\r\n\r\n",
		numBytesPerRead: 3,
	}
	rq, err := RequestFromReader(reader)
	require.NoError(t, err)
	require.NotNil(t, rq)
	assert.Equal(t, "GET", rq.RequestLine.Method)
	assert.Equal(t, "/", rq.RequestLine.RequestTarget)
	assert.Equal(t, "1.1", rq.RequestLine.HttpVersion)

	// Test: Good GET Request line with path
	reader = &chunkReader{
		data:            "GET /coffee HTTP/1.1\r\nHost: localhost:42069\r\nUser-Agent: curl/7.81.0\r\nAccept: */*\r\n\r\n",
		numBytesPerRead: 1,
	}
	rq, err = RequestFromReader(reader)
	require.NoError(t, err)
	require.NotNil(t, rq)
	assert.Equal(t, "GET", rq.RequestLine.Method)
	assert.Equal(t, "/coffee", rq.RequestLine.RequestTarget)
	assert.Equal(t, "1.1", rq.RequestLine.HttpVersion)
}

func TestParseHeaders(t *testing.T) {
	// Test: Standard Headers
	reader := &chunkReader{
		data:            "GET / HTTP/1.1\r\nHost: localhost:42069\r\nUser-Agent: curl/7.81.0\r\nAccept: */*\r\n\r\n",
		numBytesPerRead: 3,
	}
	rq, err := RequestFromReader(reader)
	require.NoError(t, err)
	require.NotNil(t, rq)
	host, ok := rq.Headers.Get("host")
	assert.True(t, ok)
	assert.Equal(t, "localhost:42069", host)
	agent, ok := rq.Headers.Get("user-agent")
	assert.True(t, ok)
	assert.Equal(t, "curl/7.81.0", agent)
	accept, ok := rq.Headers.Get("accept")
	assert.True(t, ok)
	assert.Equal(t, "*/*", accept)

	// Test: Malformed Header
	reader = &chunkReader{
		data:            "GET / HTTP/1.1\r\nHost localhost:42069\r\n\r\n",
		numBytesPerRead: 3,
	}
	rq, err = RequestFromReader(reader)
	require.Error(t, err)
}

func TestParseBody(t *testing.T) {
	// Test: Standard Body
	reader := &chunkReader{
		data: "POST /submit HTTP/1.1\r\n" +
			"Host: localhost:42069\r\n" +
			"Content-Length: 13\r\n" +
			"\r\n" +
			"hello world!\n",
		numBytesPerRead: 3,
	}
	rq, err := RequestFromReader(reader)
	require.NoError(t, err)
	require.NotNil(t, rq)
	assert.Equal(t, "hello world!\n", string(rq.Body))

	// Test: Body shorter than reported content length
	reader = &chunkReader{
		data: "POST /submit HTTP/1.1\r\n" +
			"Host: localhost:42069\r\n" +
			"Content-Length: 20\r\n" +
			"\r\n" +
			"partial content",
		numBytesPerRead: 3,
	}
	rq, err = RequestFromReader(reader)
	require.Error(t, err)
}
